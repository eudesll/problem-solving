def parse_fasta(s):
    results = {}
    strings = s.strip().split('>')

    for s in strings:
        if len(s) == 0:
            continue

        parts = s.split()
        label = parts[0]
        bases = ''.join(parts[1:])

        results[label] = bases
        
    return results


def gc_content(s):
    m = 0

    for c in s:
        if c in ['G', 'C']:
            m += 1

    return 100 * (float(m) / len(s))

