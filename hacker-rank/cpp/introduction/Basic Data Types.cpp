#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    
    int a;
    long b;
    long long c;
    char d;
    float e;
    double f;
    
    cin >> a >> b >> c >> d >> e >> f;
    
    printf("%d\n", a);
    printf("%ld\n", b);
    printf("%lld\n", c);
    printf("%c\n", d);
    printf("%f\n", e);
    printf("%lf\n", f);
    
    return 0;
}
